function [A, B] = eval_linear(A, B, zs, us)
    syms x1 x2 x3 a1 a2 a3 v1 v2 v3 w1 w2 w3
    syms u1 u2 u3 u4
    A = eval(subs(A, {x1 x2 x3 a1 a2 a3 v1 v2 v3 w1 w2 w3 u1 u2 u3 u4}, ...
                {zs(1) zs(2) zs(3) zs(4) zs(5) zs(6) zs(7) zs(8) zs(9) ...
                 zs(10) zs(11) zs(12) us(1) us(2) us(3) us(4)}));

    B = eval(subs(B, {x1 x2 x3 a1 a2 a3 v1 v2 v3 w1 w2 w3 u1 u2 u3 u4}, ...
                {zs(1) zs(2) zs(3) zs(4) zs(5) zs(6) zs(7) zs(8) zs(9) ...
                 zs(10) zs(11) zs(12) us(1) us(2) us(3) us(4)}));
end